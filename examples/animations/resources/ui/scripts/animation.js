function onButtonEnter(button) {
    button.animate("font-color", "#ff0000", "linear", 500);
    button.animate("font-size", "36px", "linear", 500);
}

function onButtonExit(button) {
    button.animate("font-color", "#000000", "linear", 500);
    button.animate("font-size", "24px", "linear", 500);
}
